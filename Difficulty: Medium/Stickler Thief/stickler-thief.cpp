class Solution {
  public:
    int findMaxSum(vector<int>& arr) {
        // code here
        if(arr.empty()) return 0;
        int prev = 0,curr = arr[0];
        for(int i = 1; i < arr.size(); i++){
            int newmax = arr[i] + prev;

            prev = curr;

            curr = max(curr,newmax);
        }
        return curr;
    }
};


